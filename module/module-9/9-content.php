</p><h2>Truyền thông Trung Quốc tỏ ra khá thận trọng khi nhận định về trận giao hữu gặp U22 Việt Nam.</h2>
<p>Để chuẩn bị cho SEA Games 30 sắp diễn ra vào cuối năm, LĐBĐ Việt Nam đã lên kế hoạch cho trận đấu giao hữa giữa&nbsp;
<a href="https://soha.vn/u22-viet-nam-chot-quan-da-trung-quoc-bo-sung-them-sao-tu-dtqg-20190904225841406.htm" target="_blank" rel="noreferrer noopener">
U22 Việt Nam</a>&nbsp;vs U22 Trung Quốc. HLV Park Hang Seo sẽ thử nghiệm để tìm ra những nhân tố mới, bên cạnh các cái tên quen thuộc như Quang Hải,
 Văn Hậu hay Đức Chinh.</p>
<p>Đội tuyển U22 Việt Nam đã hoàn thành buổi tập cuối cùng vào chiều ngày 4/9, trước khi lên đường sang Trung Quốc tham dự trận giao hữu với&nbsp;
“Những chú rồng”.&nbsp;Về phần U22 Trung Quốc, họ vừa có trận đấu giao hữu với CHDCND Triều Tiên vào ngày 5/9 và bị đối thủ cầm hoà với tỷ số 1-1.</p>
<p>Trước thềm trận giao hữu gặp Việt Nam, truyền thông Trung Quốc nhận định: “U22 Việt Nam là một đội bóng mạnh với những cầu thủ chất lượng đã được kiểm 
chứng. Mặc dù được đánh giá cao hơn nhưng không có nghĩa là chúng ta được phép chủ quan. Việc được gặp một đội bóng với lối chơi biến ảo như U22 
Việt Nam sẽ là dịp để các cầu thủ nước nhà có cơ hội học tập”, tờ Tailibao chia sẻ.</p>
<p>Bên cạnh đó, tờ tailibao còn nhận định về cơ hội của U22 Việt Nam tại SEAgames 30 sắp tới:
 “Được biết, đội Việt Nam gặp Trung Quốc là để chuẩn bị cho đại hội thể thao Đông Nam Á diễn ra vào cuối năm.</p>
<p>Việc LĐBĐ Việt Nam đẩy mạnh công tác tập huấn cũng là một nước đi đúng đắn. Với sức mạnh đã thể hiện trong những năm vừa qua, U22 Việt Nam được đánh giá
 là đội bóng mạnh nhất nhì giải”.</p>
<figure class="wp-block-image"><img src="https://sohanews.mediacdn.vn/2019/9/7/photo-1-15678428446541222848734.jpg" 
alt="Báo Trung Quốc: Găp Việt Nam là cơ hội để học tập - Ảnh 1."></figure>
<p>U22 Trung Quốc cầm hoà CHDCND Triều Tiên 1-1 trước khi đấu U22 Việt Nam</p>
<p>U22 Trung Quốc sở hữu những cái tên rất đáng chú ý như chân sút Lin Liangming, người được Real Madrid chiêu mộ vào năm 2016,
 sau đó cho Almeria mượn. Ngoài ra còn có cái tên Zhang Yuning, cầu thủ từng có thời gian thi đấu ở Đức và Hà Lan. 
 Tiền đạo sinh năm 1997 đang có phong độ cao khi ghi được 5 bàn sau 18 lần ra sân thi đấu ở Super League. 
 Anh được định giá lên tới 1,7 triệu đô la.<br></p>
<p>Trận đấu giao hữu giữa U22 Việt Nam vs U22 Trung Quốc sẽ diễn ra vào lúc 17h00 ngày 8/9,
 trên SVĐ Huangshi Olympic Sports Center, Vũ Hán, Trung Quốc.</p>
